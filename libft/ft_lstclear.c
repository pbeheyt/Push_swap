#include "libft.h"

void	ft_lstclear(t_list **lst)
{
	t_list	*tmp_prev;
	t_list	*tmp;

	if (!lst)
		return ;
	tmp = *lst;
	while (tmp)
	{
		tmp_prev = tmp;
		tmp = tmp_prev->next;
		free(tmp_prev);
	}
	*lst = NULL;
}
