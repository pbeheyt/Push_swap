#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*element;

	if (lst == 0 || del == 0)
		return ;
	while (*lst != 0)
	{
		del((*lst)->content);
		element = *lst;
		*lst = element->next;
		free(element);
	}
	*lst = 0;
}
